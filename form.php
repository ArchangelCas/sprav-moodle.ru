    <h1 class="pageTitle" align="center">Формат курса</h1>
      <p align="justify">Учебный курс в системе Moodle может быть представлен преподавателем в определенном формате, в зависимости от настроек курса.</p>
      <p align="justify">Курс в Moodle может строиться на одном из следующих форматов:</p>
      <p align="justify"><b>SCORM</b> — стандарт организации учебного материала. SCORM содержимое представляет из себя блоки учебного материала и JavaScript кода, которые могут передавать Moodle информацию об оценках студента и его прогрессе.</p>
      <p align="justify"><b>Разделы по сообществам</b> — организация курса в виде одного большого форума. Такой формат подходит для обучения в свободном (неформальном) виде.</p>
      <p align="justify"><b>Разделы по темам</b> — курс разбит на отдельные разделы (темы). Количество тем определяет преподаватель. Каждая тема наполнена различными ресурсами и активными элементами.</p>
      <p align="justify"><b>Разделы по неделям</b> — материал курса организован по неделям, с фиксированными датами начала и завершения обучения. Текущая учебная неделя курса будет выделена цветом.</p>
