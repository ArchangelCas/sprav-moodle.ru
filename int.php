    <h1 class="pageTitle" align="center">Пользовательский интерфейс</h1>
   <p align="justify">Интерфейс системы Moodle зависит от выбранного администратором шаблона оформления сайта и, в нашем случае, включает в себя:</p> 
   <p align="justify">• Полное название сайта</p>
   <p align="justify">• Выбор языка</p>
   <p align="justify">• Блок <b>Категории курсов</b></p>
   <p align="justify">• Блок <b>Новости сайта</b></p>
   <p align="justify">• Блок <b>Навигация</b></p>
   <p align="justify">• Блок <b>Вход</b></p>
   <img src="img/int.png">
   <p align="justify">Интерфейс Главной страницы может меняться администратором в зависимости от поставленных задач.</p>
   <p align="justify">Каждому пользователю предоставлена возможность выбора языка, на котором будет отображаться интерфейс системы.</p>
   <p align="justify">• Блок <b>Категории курсов</b> содержит курсы, сгруппированные по категориям. Администратор системы Moodle может задать несколько категорий курсов. Например, все предметы университета сгруппированы по кафедрам, на которых эти предметы разрабатываются. Этот выбор влияет на порядок отображения списка курсов и может облегчить нахождение слушателем своего курса.Слушатель может найти нужный курс, воспользовавшись строкой поиска курса.</p>
   <p align="justify">• Блок <b>Навигация</b> предназначен для быстрого перехода по важным разделам сайта.</p>
   <p align="justify">• Блок <b>Последние новости</b> служит для рассылки всем пользователям информационных сообщений. Новости в данном форуме размещает только администратор системы. Просматривать новости могут все пользователи. Чтобы просмотреть все главные новости и объявления, нажмите на ссылку. На Главной странице, в зависимости от настроек администратора, можно расположить от 1 до 10 новостей.</p>
   <p align="justify">• Блок <b>Вход</b> предназначен для входа в систему дистанционного обучения Moodle зарегистрированным пользователям. Для этого Вам необходимо создать учетную запись на сайте.</p>
